const month = "may"

switch (month){
    case "jan":
        console.log("January")
        break;
    case "feb":
        console.log("February")
        break;
    case "mar":
        console.log("March")
        break;
    case "apr":
        console.log("April")
        break;
    case "may":
        console.log("May")
        break;

    default:
        console.log("No Match Found!")
}